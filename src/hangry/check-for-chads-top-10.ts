export const checkForChadsTop10 = (tokenId: string, nfts: NftRepresentation[]) =>
  isBoonToken(tokenId) &&
  (checkForRebel(tokenId, nfts) ||
    nfts.find((n) => isBoonToken(n.tokenId) && topTenTraits.has(n.serialNumber)));
